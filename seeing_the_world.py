# This program is to practice list methods
locations = ['Paris', 'New York', 'London', 'Tokyo', 'Sydney']
print(locations)

# sort in alphabetical order
print(sorted(locations))

# sort in reverse alphabetical order
print(sorted(locations, reverse=True))

# original list
print(locations)

# sort in reverse
locations.reverse()
print(locations)

# sort in reverse again
locations.reverse()
print(locations)

# sort in alphabetical order
locations.sort()
print(locations)

# sort in reverse alphabetical order
locations.sort(reverse=True)
print(locations)
